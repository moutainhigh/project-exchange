	  public static String ch_coding_GBK2(String strline) {
		    String ss;
		    if (strline == null) {
		      return "";
		    }
		    ss = "";
		    try {
		      byte bb[] = strline.getBytes("latin1");
		      ss = new String(bb, "GBK");
		      return ss;
		    }
		    catch (UnsupportedEncodingException ex1) {
		      return strline;
		    }
		  }
	  
	  
