	  public static boolean isNumber(String input) {
	    if (input == null || input.length() < 1) {
	      return false;
	    }
	    String number = "1234567890";
	    for (int i = 0; i < input.length(); i++) {
	      String temp = input.substring(i, i + 1);
	      if (number.indexOf(temp) == -1) {
	        return false;
	      }
	    }
	    return true;
	  }
	  
