package eahoosoft.pojo;

public class Guide {
	
	private Long id;
	private String title;
	private String keywords;
	private String description;
	private String name;
	private String content;
	private String fileName;
	private String linkName;
	private String linkDown;
	private String linkBuy;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getKeywords() {
		return keywords;
	}
	public void setKeywords(String keywords) {
		this.keywords = keywords;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getLinkName() {
		return linkName;
	}
	public void setLinkName(String linkName) {
		this.linkName = linkName;
	}
	public String getLinkDown() {
		return linkDown;
	}
	public void setLinkDown(String linkDown) {
		this.linkDown = linkDown;
	}
	public String getLinkBuy() {
		return linkBuy;
	}
	public void setLinkBuy(String linkBuy) {
		this.linkBuy = linkBuy;
	}
	
	
}
